#include <stdio.h>

/*
1~n까지의 합을 구합니다. (do문에서 양의 정수만을 n 값으로 입력합니다.)
*/

int main(void){
	int i, n;
	int sum;
	puts("1부터 n까지의 합을 구합니다.");
	do{
		printf("n 값 :"); scanf("%d", &n);
	}while(n<=0); // n이 0보다 클 때까지 반복합니다.
	sum = 0;
	for(i=1; i<=n; i++){
		sum += i;
	} 
	printf("1부터 %d까지의 합은 %d입니다.", n, sum);
	return 0;
} 

