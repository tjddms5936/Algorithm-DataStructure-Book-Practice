// 선형 검색 for문으로  
#include <stdio.h>
#include <stdlib.h>

// 요소의 개수가 n인 배열 a에서 key와 일치하는 요소를 선형 검색
int search(const int a[], int n, int key){
	int i;
	for(i=0; i<n; i++){
		if(a[i] == key){
			return i;
		} // 검색 성공 
	}
	return -1; // 검색 실패 
} 

int main(void){
	int i, nx, ky, idx;
	int *x; // 배열의 첫 번째 요소에 대한 포인터
	puts("선형 검색");
	printf("요소 개수 :"); scanf("%d", &nx);
	x = calloc(nx, sizeof(int)); // 요소의 개수가 nx인 int형 배열을 동적 생성
	for(i=0; i<nx; i++){
		printf("x[%d] :", i); scanf("%d", &x[i]); // 요소 삽입 
	} 
	printf("검색값 :"); scanf("%d", &ky);
	idx = search(x, nx, ky); // 배열 x의 값이 ky인 요소를 선형 검색 
	if(idx==-1){ // 검색에 실패했다면
		puts("검색에 실패했습니다."); 
 	} 
 	else{ // 검색에 성공했다면
		printf("%d는 x[%d]에 있습니다.\n", ky, idx); 	 
	}
	free(x);
	 
	return 0;
} 
